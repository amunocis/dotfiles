vim.opt.number = true          -- Muestra números de línea
vim.opt.cursorline = true      -- Resalta la línea actual del cursor

-- Tabulación y espacios
vim.opt.tabstop = 4            -- Ancho de un tab
vim.opt.shiftwidth = 4         -- Ancho para indentar
vim.opt.expandtab = true       -- Usar espacios en lugar de tabs

-- Búsqueda
vim.opt.hlsearch = true        -- Resaltar resultados de búsqueda
vim.opt.incsearch = true       -- Búsqueda incremental
vim.opt.ignorecase = true      -- Ignorar mayúsculas/minúsculas al buscar
vim.opt.smartcase = true       -- A menos que la búsqueda contenga mayúsculas

vim.opt.wrap = false           -- No envolver líneas largas
